<h3>Zunftberufe</h3>
<p>
    Laut Zunftbrief von 1499 und dessen Erneuerung von 1535 sowie laut Urkunde vom 20. September 1476 waren den
    Schmieden zugeteilt das eigentliche Schmiedehandwerk sowie das freie Handwerk.
</p>
<div class="subtitle">Schmiedehandwerk:</div>
<ul>
    <li>Hufschmiede</li>
    <li>Haubenschmiede</li>
    <li>Klingenschmiede</li>
    <li>Messerschmiede</li>
    <li>Sporer</li>
    <li>Schlosser</li>
    <li>Nagler</li>
</ul>

<div class="subtitle">Freies Handwerk:</div>
<ul>
    <li>Glockengiesser</li>
    <li>Kannengiesser</li>
    <li>Hafengiesser</li>
    <li>Kupferschmied</li>
    <li>Spengler</li>
    <li>Harnischer</li>
    <li>Schwertfeger</li>
    <li>Wagner</li>
    <li>Zimmerleute</li>
    <li>Maurer</li>
    <li>Steinmetze</li>
    <li>Schleifer</li>
    <li>Tischmacher (Schreiner)</li>
    <li>Ziegler</li>
</ul>
<p>
    <b>Nur durch Zugeh&ouml;rigkeit zu einer Zunft konnten auch diese zweite Gruppe ihre politischen Rechte aus&uuml;ben. </b>
</p>
<p>
    Die Sporer r&ouml;steten Reiter aus mit Sporen, Gebissen und Steigb&uuml;geln.
    Die Harnischer (bis 16. Jh.) fertigten f&uuml;r die gepanzerten Reiter deren Brustharnische, Arm- und Beinschienen
    an. Die Nagler stellten N&auml;gel in allen Arten und Gr&ouml;ssen her.
</p>
