		&nbsp;Der neue Vulkan
		</span>
		<br><br>
		<span style="font-family:Arial;font-size:12;color:white">
		Das Zunftleben ohne Pokal war nicht von Dauer. 
        An der ao Zunftversammlung vom 01. April 1885 schl&auml;gt Heinrich Sorg, Polizei-Adjunkt die Anschaffung eines neuen Zunftpokales vor, was allgemein begr&uuml;sst wurde. Der damalige Vice-Pr&auml;sident C. Brupbacher beantragte daraufhin die Gr&uuml;ndung eines Becherfonds mit j&auml;hrlichen Zusch&uuml;ssen. Im Juli 1891 wies dieser Fonds einen Bestand von etwas &uuml;ber Fr. 1 '500.-- aus. 
        Zunftvorsteher C. Spleiss-Oechslin schl&auml;gt am 27. Januar 1890 der Vorsteherschaft vor, durch das Gewerbemuseum des Technikums Winterthur Konkurrenzpl&auml;ne oder Zeichnungen mit Pr&auml;mierung der besten Leistungen (Fr. 500.-- + 200.--) anfertigen zu lassen.
        Die Zunftversammlung vom 21. August 1890 erkl&auml;rt sich mit diesem Vorgehen einver-standen. <br><br>
        Am 19. November 1890 teilt der Zunftvorsteher der Vorsteherschaft und am 07.Dezember 1890 der Zunftversammlung mit, dass das vom Vorstand des Gewerbe-museums Z&uuml;rich- Winterthur bestellte Preisgericht, bestehend aus den Herren Prof Stadler, Z&uuml;rich, Prof Wildermuth, Winterthur, Direktor M&uuml;ller, Z&uuml;rich, und Zunftvorste-her C. Spleiss die Pr&auml;mierung der von einer grossen Anzahl von Bewerbern vorgeleg-ten Zeichnungen und Modelle eines Zunftpokals der Zunft zu'n Schmieden vorgenom-men habe. Die erste Pr&auml;mie erhielt ein Herr Morf, Sch&uuml;ler am Polytechnikum. 
        Eine weitere von der Zunftvorsteherschaft am 03. Dezember 1890 bestellte Experten-kommission hatte die f&uuml;r die Zunft am passendsten scheinenden Vorlagen zu bezeich-nen. 
        Am 06. Dezember 1890 einigte sich die Kommission, die von Herrn Morf angefertigte, erstpr&auml;mierte Vorlage zur Ausf&uuml;hrung zu empfehlen (mit Begr&uuml;ndung). 
        <br><br>
		Die gew&uuml;nschten Ab&auml;nderungen: 
        -	Wegfall der h&auml;ngenden Verzierungen unterhalb des eigentlichen Bechers
        -	Anbringung des Datums der Stiftung, des Namens der Zunft und des Stadtwap-pens am Leib des Bechers
        -	Platzierung eines Schmiedes nach der seinerzeit angefertigten Fotografie anstelle des sich auf das Stadtwappen st&uuml;tzenden Bockes auf dem Deckel des Bechers k&ouml;nnten ohne Beeintr&auml;chtigung des k&uuml;nstlerischen Wertes vorgenommen werden. 
        <br><br>
        Am 7. Dezember 1890 akzeptiert die Zunftversammlung diese Empfehlung: 
        -	Die Skizzen zu den Ab&auml;nderungen sollen Herrn Jezler, Zeichnungslehrer am Gym-nasium zur Ausf&uuml;hrung &uuml;bergeben werden zwecks Weiterleitung und Bearbeitung der endg&uuml;ltigen Vorlage durch den damals in Italien weilenden Herrn Morf. 
        <br><br>
        Zeichnungslehrer Jezler berichtet am 16. Februar 1891, dass die Zeichnung von Herrn Morf nichts anderes sei als eine Kopie der Holbeinschen Zeichnung; er unterlasse deshalb die ihm aufgegebenen Skizzen. Die Vorsteherschaft beschliesst am 23. Feb-ruar 1891, Herrn Jezler dennoch zu beauftragen, bis zur n&auml;chsten Zunftversammlung eine Vorlage mit den gew&uuml;nschten Ab&auml;nderungen zu beauftragen. An der Sitzung der Vorsteherschaft vom 11. Januar 1892 wird die von Carl Jezler abge&auml;nderte Pokal-zeichnung vorgelegt und im allgemeinen als befriedigend beurteilt. Am 15.  Januar 1891 schloss sich die um drei Sachverst&auml;ndige (u.a. C. Jezler) erweiterte Vorsteher-schaft der Meinung von C. Jezler an, dass ein Pokal nach dessen Vorlage eben doch eine Nachahmung des Holbeinschen Bechers sei und bleibe. Die Herren Jezler (Zeich-nungslehrer), Jezler (Silberarbeiter), Eugen Uelinger (Maler) und Stamm (Architekt) werden ersucht, binnen k&uuml;rzester Frist Pokalvorlagen anzufertigen. Eine weitere Aus-schreibung wird abgelehnt.
         <br><br>
        An der Sitzung der Vorsteherschaft vorn 01. Mai 1892 spricht sich die Mehrheit f&uuml;r einen Vulkan in Form einer Figur, wie das Modell des Mitz&uuml;nftigen Eugen Uehlinger sie zeige, aus. Am 08. Mai 1892 beschliesst die Zunftversammlung im Gasthof zur Krone mit Einmuth, einen Zunftpokal in Form eines Vulkans, einen Schmied vor dem Amboss stehend darstellend, durch die Silberwarenfabrik Jezler herstellen zu lassen. 
        Mitglieder vor Vorsteherschaft waren damals (laut Wahlen vom 30. August 1888, 24. August 1889 und 18. August 1892) neben Zunftvorsteher C. Spleiss-Oechslin, Vice-Pr&auml;sident C. Brupacher, Zunftverwalter J. Uehlinger, Zunftschreiber J.H. Sorg, 1. Bei-sitzer J. Waldvogel und 2. Beisitzer G. Sigg-Br&auml;ndli. Die Namen dieser Personen fin-den sich alle am Rand des Becherdeckels wie auch am Sockel des Vulkans eingra-viert. 
        Die Pokalzeichnung - erworben vom Schweiz. Landesmuseum in Z&uuml;rich - stammt also von Zeichnungslehrer Carl Jezler, Schaffhausen, und muss in der Zeit von M&auml;rz bis Dezember 1891 entstanden sein. 
        Am Aschermittwochanlass 1893 der Schmieden ist der neue Vulkan w&uuml;rdig eingeweiht worden; f&uuml;r die erwarteten 120 Teilnehmer waren 50 FI. Wein a Fr. 1.50 zum F&uuml;llen des Pokals vorgesehen...Der neue Silberschmied kostete laut Becherrechnung 1894/95 Fr. 2'800.--; f&uuml;r Modell, 2 Zeichnungen Etui etc. wurden weitere total Fr. 700.-- ausgegeben. 
